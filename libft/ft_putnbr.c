#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n >= -2147483647 && n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	else if (n <= 2147483647 && n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else if (n <= 9)
	{
		if (n >= 0)
			ft_putchar(n + '0');
	}
}
